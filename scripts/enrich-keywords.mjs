let patched = 0;
for (const major of majors) {
  const extra = EXTRA[major.id];
  if (!extra) continue;

  // 기존 키워드와 합치되 중복 제거
  const merged = Array.from(new Set([...major.keywords, ...extra]));
  if (merged.length !== major.keywords.length) {
    major.keywords = merged;
    patched++;
  }
}

writeFileSync(filePath, JSON.stringify(majors, null, 2), "utf8");

